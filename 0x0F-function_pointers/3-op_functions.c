#include "3-calc.h"

/**
 * op_add - sum of two integers
 * @a: int one
 * @b: int two
 *
 * Return: (a + b) which is the result of summition
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference between two integers
 * @a: int one
 * @b: int two
 *
 * Return: (a - b) which is the result of difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of two integers
 * @a: int one
 * @b: int two
 *
 * Return: (a * b)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two integers
 * @a: int one
 * @b: int two
 *
 * Return: (a / b) which is the result of divison
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder ot the division of two integers
 * @a: int one
 * @b: int two
 *
 * Return: (a % b) which is the remainder ot the division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
